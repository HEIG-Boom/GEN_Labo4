package ch.heigvd.gen.labo4;

import ch.heigvd.gen.labo4.color.Color;

public class Product {
    private static final int SIZE_NOT_APPLICABLE = -1;
    private String code;
    private Color color;
    private int size;
    private double price;
    private String currency;

    public Product(String code, Color color, int size, double price, String currency) {
        this.code = code;
        this.color = color;
        this.size = size;
        this.price = price;
        this.currency = currency;
    }

    public static int getSizeNotApplicable() {
        return SIZE_NOT_APPLICABLE;
    }

    public String getCode() {
        return code;
    }

    public int getSize() {
        return size;
    }

    public double getPrice() {
        return price;
    }

    public String getCurrency() {
        return currency;
    }

    public String getSizeFor() {
        switch (getSize()) {
            case 1:
                return "XS";
            case 2:
                return "S";
            case 3:
                return "M";
            case 4:
                return "L";
            case 5:
                return "XL";
            case 6:
                return "XXL";
            default:
                return "Invalid Size";
        }
    }

    String getColor() {
        return color.toString();
    }

    public void getProductContent(StringBuffer sb) {
        sb.append("{");
        sb.append("\"code\": \"");
        sb.append(getCode());
        sb.append("\", ");
        sb.append("\"color\": \"");
        sb.append(getColor());
        sb.append("\", ");

        if (getSize() != Product.getSizeNotApplicable()) {
            sb.append("\"size\": \"");
            sb.append(getSizeFor());
            sb.append("\", ");
        }

        sb.append("\"price\": ");
        sb.append(getPrice());
        sb.append(", ");
        sb.append("\"currency\": \"");
        sb.append(getCurrency());
        sb.append("\"}, ");
    }
}